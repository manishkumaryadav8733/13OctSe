# Create a list with multiple data types
my_list = [10, "Hello", 3.14, True]

# Access elements using positive indexing
print(my_list[0])   # First element → 10
print(my_list[1])   # Second element → "Hello"

# Access elements using negative indexing
print(my_list[-1])  # Last element → True
print(my_list[-2])  # Second last element → 3.14

# Slicing a list
print(my_list[1:3])  # Elements from index 1 to 2 → ["Hello", 3.14]